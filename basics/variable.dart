// Dart supports the following built-in Data types.

  // Number :
    // Integer - Integer values represent the whole number or non-fractional values.
    // An integer data type represents the 64-bit non-decimal numbers between -263 to 263.
    // A variable can store an unsigned or signed integer value.
    // The example is given below 
  int note = 20 ;
  int speed = 297434334 ;


  // double :
    //Double value represents the 64-bit of information (double-precision) 
    //for floating number or number with the large decimal points. 
    //The double keyword is used to declare the double type variable.

  double pi = 3.14 ;
  double height = 29.982 ;

  // Boolean
    // The Boolean type represents the two values - true and false. The bool keyword uses to denote Boolean Type.
    // The numeric values 1 and 0 cannot be used to represent the true or false value.

    bool open = true ;
    bool close = false ;


  // Strings
    // A string is the sequence of the character. If we store the data like - name, address, special character, etc.
    // It is signified by using either single quotes or double quotes. A Dart string is a sequence of UTF-16 code units.
  String name = ' khaled ' ;
  String last_name = " khalil " ;

  // Var
    // In dart var can be all data type 
  var nname = "dart";
  var width = 12.12 ;
  var cond = true ;
  var agge = 20 ;

  // Num
    // num can be integer and double
  num grade = 12 ;
  num x = 10.10 ;

  // const & Final
    // When we do not want to change a variable in the future then we use final and const.
    // It can be used in place of var or in addition to a type. 
    // A final variable can be set only one time where the variable is a compile-time constant.
    // The example of creating a final variable is given below.

  final namee = 'Ricky';                  // final variable without type annotation.  
  final String msg = 'How are you?';     // final variable with type annotation. 

  // namee = 'Roger';                       // Error: Final variable can't be changed.  

  const cst = 100 ;
  // cst = 20 ;                          // Error: const variable can't be changed.


  
