void main() {
  // a = 20       b = 10

  // 	Addition(+)	It adds the left operand to the right operand.	a+b will return 30

  int a = 20;
  int b = 10;

  print(a + b);

// Subtraction(-)	It subtracts the right operand from the left operand. a-b will return 10

  print(a - b);

// Divide(/)	It divides the first operand by the second operand and returns quotient.	a/b will return 2.0

  print(a / b);

//Multiplication(*)	It multiplies the one operand to another operand.	a*b will return 200

  print(a * b);

// Modulus(%)	It returns a reminder after dividing one operand to another.	a%b will return 0

  print(a % b);

// Division(~/)	It divides the first operand by the second operand and returns integer quotient.	a/b will return 2

  print(a ~/ b);

// Unary Minus(-expr)	It is used with a single operand changes the sign of it.	-(a-b) will return -10

  print(-(a - b));
}
